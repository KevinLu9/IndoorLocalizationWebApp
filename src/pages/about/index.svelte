  <p>
    This application was created for a Final Year Engineering Project at Monash University.
    The FYP was hosted by the Electrical and Computer Systems Engineering (ECSE) department.
    The aim of the project was to create an interactive and engaging platform that allows users to see informative content based on their proximity to certain locations.<br/> <br/>
    This project addresses the challenge of accurate indoor localization for targeted advertising without requiring users to carry additional dedicated hardware. 
    It leverages the availability of smartphones and the capabilities they offer, making the application accessible through web browsers rather than requiring native app installations. 
    This approach aligns well with the current reliance on smartphones as much of the population own these devices.
